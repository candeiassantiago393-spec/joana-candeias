const STORAGE_KEY = 'joana-site-lang';
let currentLang = 'pt';

function t(key) {
  return TRANSLATIONS[currentLang]?.[key] ?? TRANSLATIONS.pt[key] ?? key;
}

function getLang() {
  return currentLang;
}

function setLang(lang) {
  if (!TRANSLATIONS[lang] || lang === currentLang) return;
  currentLang = lang;
  localStorage.setItem(STORAGE_KEY, lang);
  applyLanguage();
  document.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
}

function applyLanguage() {
  document.documentElement.lang = currentLang === 'en' ? 'en' : 'pt';

  const title = t('meta.title');
  document.title = title;

  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) metaDesc.content = t('meta.description');

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });

  document.querySelectorAll('[data-i18n-html]').forEach((el) => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });

  document.querySelectorAll('[data-i18n-aria]').forEach((el) => {
    el.setAttribute('aria-label', t(el.dataset.i18nAria));
  });

  document.querySelectorAll('select option[data-i18n]').forEach((opt) => {
    const label = t(opt.dataset.i18n);
    opt.textContent = label;
    if (opt.value !== '') opt.value = label;
  });

  document.querySelectorAll('.lang-btn').forEach((btn) => {
    const active = btn.dataset.lang === currentLang;
    btn.classList.toggle('is-active', active);
    btn.setAttribute('aria-pressed', String(active));
  });
}

function initLanguage() {
  const saved = localStorage.getItem(STORAGE_KEY);
  if (saved && TRANSLATIONS[saved]) currentLang = saved;

  const switcher = document.getElementById('langSwitch');
  switcher?.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.addEventListener('click', () => setLang(btn.dataset.lang));
  });

  applyLanguage();
}

window.SiteI18n = { t, getLang, setLang, applyLanguage, initLanguage };

document.addEventListener('DOMContentLoaded', initLanguage);
